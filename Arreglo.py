##Array de objetos
motos = ["yamaha", "honda", "suzuki", "kawasaki", "TVS",["Royal", "KTM"]]

#Elimina e imprime el objeto eliminado
print(motos.pop(0))

##Elimina el valor especificado
motos.remove("suzuki")
print(motos)

##Elimina el valor especificado por posicion 
del motos[3]
print(motos)

##Elimina la lista completa
motos.clear()
print(motos)




